void fbdraw_pixel(unsigned char* pixel, int r, int g, int b) {
    pixel[0] = r; // B
    pixel[1] = g; // G
    pixel[2] = b; // R
    pixel[3] = 0; // Unused
}

/* Clean the entire screen */
void fbclear() {
    for (int y = 0; y < fb_vinfo.yres; y++) {
        unsigned char* row_base = framebuffer + (y + fb_vinfo.yoffset) * fb_finfo.line_length;
        for (int x = 0; x < fb_vinfo.xres; x++) {
            unsigned char* pixel = row_base + (x + fb_vinfo.xoffset) * 4; // 32bpp
            fbdraw_pixel(pixel, 0, 0, 0);
        }
    }
}

